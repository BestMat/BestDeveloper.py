def isInt(a):
  if type(a) != int:
    print("Not a INT")
def isStr(a):
  if type(a) != str:
    print("Not a STRING")
def isFloat(a):
  if type(a) != float:
    print("Not a FLOAT")
def isList(a):
  if type(a) != list:
    print("Not a LIST/ARRAY")
def isTuple(a):
  if type(a) != tuple:
    print("Not a TUPLE")
def isDict(a):
  if type(a) != dict:
    print("Not a JSON/DICT")
  
  
